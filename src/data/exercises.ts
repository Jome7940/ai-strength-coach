// Helper functions
export function getExercisesByMuscle(muscle: MuscleGroup): Exercise[] {
  return exercises.filter(
    e => e.primaryMuscles.includes(muscle) || e.secondaryMuscles.includes(muscle)
  );
}

export function getExercisesByPattern(pattern: MovementPattern): Exercise[] {
  return exercises.filter(e => e.movementPattern === pattern);
}

export function getExercisesByEquipment(equipment: Equipment[]): Exercise[] {
  return exercises.filter(
    e => e.equipment.some(eq => equipment.includes(eq))
  );
}

export function getExercisesByDifficulty(difficulty: 'beginner' | 'intermediate' | 'advanced'): Exercise[] {
  return exercises.filter(e => e.difficulty === difficulty);
}

export function getWarmupExercises(): Exercise[] {
  return exercises.filter(e => e.category === 'warmup');
}

export function getCompoundExercises(): Exercise[] {
  return exercises.filter(e => e.category === 'compound');
}

export function getIsolationExercises(): Exercise[] {
  return exercises.filter(e => e.category === 'isolation');
}

export function getCoreExercises(): Exercise[] {
  return exercises.filter(e => e.category === 'core');
}
