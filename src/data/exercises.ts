// Helper to create exercises with consistent defaults
function createExercise(
  id: string,
  name: string,
  primaryMuscles: MuscleGroup[],
  secondaryMuscles: MuscleGroup[],
  movementPattern: MovementPattern,
  equipment: Equipment[],
  difficulty: 'beginner' | 'intermediate' | 'advanced',
  category: ExerciseCategory,
  defaultSets: number,
  defaultReps: string,
  defaultRpe: number,
  estimatedTimeSeconds: number,
  instructions: string,
  cues: string[] = [],
  variations: string[] = [],
  regressions: string[] = [],
  progressions: string[] = [],
  contraindications: string[] = []
): Exercise {
  return {
    id,
    name,
    primaryMuscles,
    secondaryMuscles,
    movementPattern,
    equipment,
    difficulty,
    category,
    instructions,
    cues,
    variations,
    regressions,
    progressions,
    contraindications,
    defaultSets,
    defaultReps,
    defaultRpe,
    estimatedTimeSeconds,
  };
}

